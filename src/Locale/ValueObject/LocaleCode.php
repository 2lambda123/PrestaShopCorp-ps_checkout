    /**
     * @var string
     */
    private $localeCode;

    /**
     * @param string $localeCode
     *
     * @throws LocaleException
     */
    public function __construct($localeCode)
    {
        $this->localeCode = $this->assertLocaleCodeIsValid($localeCode);
    }

    /**
     * @return string
     */
    public function getValue()
    {
        return $this->localeCode;
    }

    /**
     * @param $localeCode
     *
     * @return string
     *
     * @throws LocaleException
     */
    public function assertLocaleCodeIsValid($localeCode)
    {
        if (!is_string($localeCode)) {
            throw new LocaleException(sprintf('CODE is not a string (%s)', gettype($localeCode)), LocaleException::WRONG_TYPE_CODE);
        }
        if (!in_array($localeCode, self::LOCALE_CODE_AVAILABLE)) {
            throw new LocaleException("Invalid code ($localeCode)", LocaleException::INVALID_CODE);
        }

        return $localeCode;
    }
